package com.ss.pojo.restservice;

public class TestProcedureInfo {

    private Integer elementId;
    private String description;
    private Integer shieldElementId;
    private String shieldElementName;
    private Integer sourceId;
    private String sourceName;
    private String referenceId;

    public Integer getElementId() {
        return elementId;
    }

    public void setElementId(Integer elementId) {
        this.elementId = elementId;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public Integer getShieldElementId() {
        return shieldElementId;
    }

    public void setShieldElementId(Integer shieldElementId) {
        this.shieldElementId = shieldElementId;
    }

    public String getShieldElementName() {
        return shieldElementName;
    }

    public void setShieldElementName(String shieldElementName) {
        this.shieldElementName = shieldElementName;
    }

    public Integer getSourceId() {
        return sourceId;
    }

    public void setSourceId(Integer sourceId) {
        this.sourceId = sourceId;
    }

    public String getSourceName() {
        return sourceName;
    }

    public void setSourceName(String sourceName) {
        this.sourceName = sourceName;
    }

    public String getReferenceId() {
        return referenceId;
    }

    public void setReferenceId(String referenceId) {
        this.referenceId = referenceId;
    }
}
