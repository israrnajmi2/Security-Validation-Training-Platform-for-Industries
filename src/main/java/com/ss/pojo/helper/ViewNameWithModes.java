package com.ss.pojo.helper;

import java.util.List;

public class ViewNameWithModes {
    private String viewName;
    private List<String> selectionModes;

    public String getViewName() {
        return viewName;
    }

    public void setViewName(String viewName) {
        this.viewName = viewName;
    }

    public List<String> getSelectionModes() {
        return selectionModes;
    }

    public void setSelectionModes(List<String> selectionModes) {
        this.selectionModes = selectionModes;
    }
}
